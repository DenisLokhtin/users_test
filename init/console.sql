create database if not exists UsersTest;

use UsersTest;

DROP TABLE IF EXISTS UsersTest.Friends;
DROP TABLE IF EXISTS UsersTest.Users;

create table if not exists Users
(
    id        int auto_increment
    primary key,
    firstName varchar(255)                  unique                   not null,
    gender    enum ('male', 'female') default (_utf8mb4'male') null
    );


create table if not exists Friends
(
    id       int auto_increment,
    userId   int not null,
    friendId int not null,
    constraint friends_pk
        primary key (id),
    constraint friends_Users_null_fk
        foreign key (friendId) references Users (id),
    constraint users_friends_null_fk
        foreign key (userId) references Users (id)
);

